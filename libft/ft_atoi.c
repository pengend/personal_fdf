#include "libft.h"

int ft_atoi(const char *s1)
{
	int n;
	int signe;

	signe = 1;
	while (*s1 == '\n' || *s1 == '\r' || *s1 == '\v'
		|| *s1 == '\f' || *s1 == ' ' || *s1 == '\t')
		s1++;
	if (*s1 == '+')
		s1++;
	else if (*s1 == '-')
	{
		signe = -1;
		s1++;
	}
	n = 0;
	while (*s1 >= '0' && *s1 <= '9')
	{
		n *= 10;
		n += *s1 - 48;
		s1++;
	}
	n *= signe;
	return (n);
}
